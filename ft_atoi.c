#include "libft.h"

int	ft_atoi(const char *str)
{
	int				sign;
	unsigned long	result;
	int				i;

	result = 0;
	sign = 1;
	i = 0;
	while (str[i] && (str[i] == '\f' || str[i] == '\t' || str[i] == ' '
			|| str[i] == '\n' || str[i] == '\r' || str[i] == '\v'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		result *= 10;
		result += str[i] - '0';
		i++;
	}
	if (result > LONG_MAX)
	{
		if (sign < 0)
			return (0);
		return (-1);
	}
	result *= sign;
	return (result);
}
